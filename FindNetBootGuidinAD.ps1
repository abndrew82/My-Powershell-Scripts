Param (
       [parameter(Mandatory=$True,HelpMessage='Enter MAC Address in xx-xx-xx-xx-xx-xx Format')]
       [string]$myString = ''
       )

#$myString= Read-Host -Prompt 'Enter MAC Address in xx-xx-xx-xx-xx-xx Format'
$arr = $myString -split '-'
$part1 = [Convert]::ToInt32($arr[0],16)
$part2 = [Convert]::ToInt32($arr[1],16)
$part3 = [Convert]::ToInt32($arr[2],16)
$part4 = [Convert]::ToInt32($arr[3],16)
$part5 = [Convert]::ToInt32($arr[4],16)
$part6 = [Convert]::ToInt32($arr[5],16)
$computers = get-adcomputer -Filter {ObjectClass -Like "Computer"} -SearchBase 'OU=COEDEAN,OU=COE,OU=NCSU,DC=wolftech,DC=ad,DC=ncsu,DC=edu' | Select -ExpandProperty Name
ForEach ($computer in $computers)
{
$CompName = Get-ADComputer $computer -Properties name | Select -ExpandProperty Name
$compNetbootguid = Get-ADComputer $computer -Properties netbootguid | select -ExpandProperty netbootguid
#Write-Host $compNetbootguid
If ($compNetbootguid -like $part1 -and $compNetbootguid -like $part2 -and $compNetbootguid -like $part3 -and $compNetbootguid -like $part4 -and $compNetbootguid -like $part5 -and $compNetbootguid -like $part6)
{
#Write-Host $CompName "|" $compNetbootguid
$arr2 = $compNetbootguid -split ' '
$mac1 = “{0:x}” -f [Int]$arr2[10]
$mac2 = “{0:x}” -f [Int]$arr2[11]
$mac3 = “{0:x}” -f [Int]$arr2[12]
$mac4 = “{0:x}” -f [Int]$arr2[13]
$mac5 = “{0:x}” -f [Int]$arr2[14]
$mac6 = “{0:x}” -f [Int]$arr2[15]
Write-Host $CompName "|" $mac1 $mac2 $mac3 $mac4 $mac5 $mac6
}
}
